package com.syntax.class08;

public class Forloop {

	public static void main(String[] args) {
		int i;
		
		for(i=1;i<=5;i++) {
			
			System.out.println("Good Morning");
		}
		System.out.println("Print between 1 to 100 number");
			
		for (int a=1;a<=100;a++) {
			System.out.print(a+" ");
		}
		System.out.println("");
		System.out.println("I want to print 10 to 0 !!");
		for(int z=10; z>=0; z--) {
			System.out.print(z+" ");
		}

	}

}
