package com.syntax.class07;

public class WhilevsDoWhile {

	public static void main(String[] args) {
		System.out.println("--------------------While Loop-------------------");
		
		int a=10;
		
		while(a<15) {
			System.out.println("Hello");
			a++;
		}
		System.out.println("--------------------Do While Loop-------------------");
		int b=20;
		do {
			System.out.println("Hello");
			b++;
		}while(b<15);
		
		
	}

}
