package com.syntax.class07;

public class WhileLoopIntro {

	public static void main(String[] args) {
		
		int time =15;
		if(time >=15) {
			System.out.println("Good day");
		}
		
		System.out.println("------------ While Loop ------------");
		
		while (time > 12) {
			
			System.out.println("Good day");
			time--;
		}
	}

}
