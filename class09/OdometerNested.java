public class OdometerNested {

	public static void main(String[] args) {
		for(int i=0; i<=9; i++) {
			for(int j=0; j<=9;j++) {
				for(int k=0; k<=9;k++) {
					for(int a=0;a<=9;a++) {
						for(int b=0;b<=9;b++) {
							System.out.println(i+""+j+""+k+""+a+""+b);
							
							
						}
					}
				}
				
			}
		}
	}

}
